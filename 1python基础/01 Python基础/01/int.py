#创建一个整型变量

#十进制数字 0～9
intval = 250
#打印变量的值
print(intval)

#打印id标识
print(id(intval))

#打印类型
print(type(intval))


#二进制(BINARY)创建整型 0～1

intval2 = 0b1110011
#打印变量值
print(intval2)
#打印变量标志
print(id(intval2))
#打印变量的类型
print(type(intval2))


#八进制(OTC)创建整型  0～7
intval8 = 0o777

#打印变量值
print(intval8)
#打印变量标志
print(id(intval8))
#打印变量类型
print(type(intval8))


#十六进（HEX）制声明整型 0～9 A～F
intval16 = 0x13AF

#打印变量的值
print(intval16)
#打印变量的标志
print(id(intval16))
#打印变量类型
print(type(intval16))
