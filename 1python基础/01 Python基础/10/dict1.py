#声明一个字典

#in
dict1 = {'BOSS':'王晶晶','TEACHER':'丛浩','BANZHANG':'马艺楠'}

result = 'BOSS' in dict1

print(result)

#not in

result = 'TEACHER2' not in dict1

print(result)


