#str 字符串类型转换

var  = 998#整型  '998'
var  = 12.34#浮点数 '12.34'
var  = False#布尔值 'False'
var  = 5 + 2j#复数 '(5 + 2j)'

var = ['张飞','岳飞','王菲']#列表 "['张飞','岳飞','王菲']"
var = ('张飞','张宝','张苞','张郎') #元组 "('张飞','张宝','张苞','张郎')"
var = {'shuai':'任泽权','face':'张鹏程'}#字典 "{'shuai':'任泽权','face':'张鹏程'}"
var = {'小王','小张','小朱','小吕'} #集合 "{'小王','小张','小朱','小吕'}"

#输出原有类型和值
print(type(var),var)

#类型
newvar = str(var)

#输出转换之后的类型和值
print(type(newvar),newvar)
