#赋值运算

#普通赋值

var = 199

print(var)


#加法赋值
var = 998

var += 100 #var = var + 100

print(var)

#减法赋值

var = 888

var -= 222#var = var - 222

print(var)

#乘法赋值

var = 15

var *= 10#var = var * 10

print(var)

#除法赋值

var = 99

var /= 9# var = var / 9

print(var)


#取余等于
var = 15

var %= 6# var = var % 6

print(var)


#取商等于

var = 100

var //= 20#var = var // 20

print(var)

#幂等于

var = 3

var **= 4

