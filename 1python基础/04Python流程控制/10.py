#逻辑运算

#逻辑与运算（有假则假）

var1 = False
var2 = False

result = var1 and var2

print(result)


#逻辑或运算(有真则真)

var1 = False
var2 = False

result = var1 or var2

print(result)

#逻辑非运算
var1 = True

result = not var1

print(result)


