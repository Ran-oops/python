#首字母大写capitalize  常用于英文段落！中文没用

str1 = 'make love goodbye  干完就走'

result = str1.capitalize()

print(result)


#标题化字符串 title  常用语英文标题，中文没用

str1 = 'make love goodbye  干完就走'

result = str1.title()

print(result)


#全部转化为大写 upper
str1 = 'make Love goodbye  干完就走'

result = str1.upper()

print(result)

#全部转化为小写 lower
str1 = 'MAKE LOVE GOODBYE  干完就走'

result = str1.lower()

print(result)

#大小写互换swapcase

str1 = 'MAKE LOVE goodbye  干完就走'

result = str1.swapcase()

print(result)
