'''
单向分支

if 条件表达式:
    一行python代码
    一行python代码
    一行python代码
    ...
else:
    一行python代码
    一行python代码
    一行python代码
    ...


'''

zhangqiaoling = False

if zhangqiaoling == True:
    print('巧玲老师是个True,没有对象')
    print('巧玲老师是个True,没有对象')
    print('巧玲老师是个True,没有对象')
else:
    print('巧玲老师是个False,有对象')
    print('巧玲老师是个False,有对象')
    print('巧玲老师是个False,有对象')
#顺序结构
print('巧玲老师是个False,有对象111')
